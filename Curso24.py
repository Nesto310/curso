Pergunta1 = input("Qual numero da semana é ? [1-domingo/2-segunda... e assim vai]")

if Pergunta1 == "1":
    print("Uau hoje é Domingo!")
elif Pergunta1 == "2":
    print("Uau hoje é Segunda-feira!")
elif Pergunta1 == "3":
    print("Uau hoje é Terça-feira!")
elif Pergunta1 == "4":
    print("Uau hoje é Quarta-feira!")
elif Pergunta1 == "5":
    print("Uau hoje é Quinta-feira!")
elif Pergunta1 == "6":
    print("Uau hoje é Sexta-feira!")
elif Pergunta1 == "7":
    print("Uau hoje é Sabado!")
else:
    print("Valor invalido.")