
Pergunta = input("Em que turno você estuda ? [M/T/N] (Manhã/Tarde/Noite)")

if Pergunta == "M":
    print("Uau, você estuda de manhã")
elif Pergunta == "T":
    print("Uau, você estuda de tarde")
elif Pergunta == "N":
    print("Uau, você estuda de noite")
else:
    print("Você deve colocar M para de manhã/T para de tarde/N para de noite.")